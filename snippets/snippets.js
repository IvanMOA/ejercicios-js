// Propiedad: length (longitud de la cadena)
let mensaje = "Hola, mundo!";
console.log(mensaje.length);  // Resultado: 12

// Método: includes (verifica si contiene una palabra)
console.log(mensaje.includes("mundo"));  // Resultado: true
console.log(mensaje.includes("adiós"));  // Resultado: false

// Acceder a caracteres individuales usando corchetes []
console.log(mensaje[0]);  // Resultado: "H"
console.log(mensaje[7]);  // Resultado: "m"
